	                <h3 class="text-2xl font-bold my-5">What is a PIP?</h3>

	                <p class="text-base leading-8 my-5">
	                    You need to understand what a PIP is and how to calculate its value to  understand what you are trading. PIP stands for Percentage in Point, and it is the  minimum price change that a specific exchange rate can make. An increase or  decrease in PIPS represents a gain or a loss in your trade. 
	                </p>

	                <p class="text-base leading-8 my-5">
	                	Usually, currencies are quoted to the fourth decimal place (for example:  EUR/USD 1.1103). There are exceptions, like the pairs that include the Japanese Yen  (JPY), or some commodities such as gold or oil. These are only quoted to the second  decimal place (for example: USD/JPY 107.98). 
	                </p>

	                <p class="text-base leading-8 my-5">
	                	When, for example, the EUR/USD pair moves from 1.1103 to 1.1104, it has  moved one PIP, because the fourth decimal place has increased by 1. For the USD/JPY,  a 1 PIP increase would be the following: from 107.98 to 107.99, because the second  decimal place has increased by one. 
	                </p>

